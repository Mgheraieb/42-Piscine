#include <stdio.h>

int		match(char *s1, char *s2)
{
	if (*s2 == '*')
		return (match(s1, ++s2));
	else if (*s1 != *s2 && *s1 != '\0' && *(--s2) == '*')
		return (match(++s1, s2));
	else if (*s1 == *s2 && (*s1 || *s2))
		return (match(++s1, ++s2));
	if (*s1 - *s2 == 0)
		return (1);
	else
		return (0);
}
