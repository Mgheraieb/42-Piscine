char	*ft_strstr(char *str, char *to_find)
{
	int i;
	int j;

	i = 0;
	if (str[0] == '\0')
		return (0);
	while (str[i] != '\0')
	{
		j = 0;
		while (str[j + i] == to_find[j])
		{
			if (to_find[j + 1] == '\0')
				return (str + i);
			j++;
		}
		i++;
	}
	return (0);
}
